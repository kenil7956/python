# Arithmetic Operators
a = 4
b = 2

print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a % b) # Modulus
print(a ** b) # Exponentiation
print(a // b) # Floor division

# Assignment Operators
a = b = c = d = e = f = g = h = 4
b += 2
c -= 2
d *= 2
e /= 2
f %= 2
g **= 2
h //= 2

print(a, b, c, d, e, f, g, h)

# Comparison Operators
x = 2
y = 4

a = x == y
g = x != y
c = x > y
d = x < y
e = x >= y
f = x <= y
print(a, b, c, d, e, f)

